package com.practice.spring;

public class Apple {

	private String name;
	private int imei;
	private double price;
	private Process a12;
	
	
	
	public Process getA12() {
		return a12;
	}
	public void setA12(Process a12) {
		this.a12 = a12;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getImei() {
		return imei;
	}
	public void setImei(int imei) {
		this.imei = imei;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public Apple(String name, int imei, double price) {
		super();
		this.name = name;
		this.imei = imei;
		this.price = price;
	}
	public Apple() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "Apple [name=" + name + ", imei=" + imei + ", price=" + price + ", a12=" + a12 + "]";
	}
	
	
}
